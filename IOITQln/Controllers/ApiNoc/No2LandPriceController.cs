        [HttpPost("ExportExcel")]
        public async Task<IActionResult> ExportExcel()
        {
            string accessToken = Request.Headers[HeaderNames.Authorization];
            Entities.Token token = (from t in _context.Tokens where Convert.ToString(t.AccessToken) == accessToken select t).FirstOrDefault();
            if (token == null)
            {
                return Unauthorized();
            }

            var identity = (ClaimsIdentity)User.Identity;
            int userId = int.Parse(identity.Claims.Where(c => c.Type == "Id").Select(c => c.Value).SingleOrDefault());
            string access_key = identity.Claims.Where(c => c.Type == "AccessKey").Select(c => c.Value).SingleOrDefault();

            if (!CheckRole.CheckRoleByCode(access_key, functionCode, (int)AppEnums.Action.EXPORT))
            {
                return new FileContentResult(new byte[0], "application/octet-stream");
            }

            List<No2LandPrice> data = _context.No2LandPricies.Where(p => p.Status != AppEnums.EntityStatus.DELETED).ToList();

            // khởi tạo wb rỗng
            XSSFWorkbook wb = new XSSFWorkbook();
            // Tạo ra 1 sheet
            ISheet sheet = wb.CreateSheet();

            string template = @"NOCexcel/bang_gia_dat_so_2.xlsx";
            string webRootPath = _hostingEnvironment.WebRootPath;
            string templatePath = Path.Combine(webRootPath, template);

            MemoryStream ms = WriteDataToExcel(templatePath, 0, data);
            byte[] byteArrayContent = ms.ToArray();
            return new FileContentResult(byteArrayContent, "application/octet-stream");
        }

        private static MemoryStream WriteDataToExcel(string templatePath, int sheetnumber, List<No2LandPrice> data)
        {
            FileStream file = new FileStream(templatePath, FileMode.Open, FileAccess.Read);
            XSSFWorkbook workbook = new XSSFWorkbook(file);
            ISheet sheet = workbook.GetSheetAt(sheetnumber);
            int rowStart = 2;

            if (sheet != null)
            {
                int datacol = 12;
                try
                {
                    //style body
                    List<ICellStyle> rowStyle = new List<ICellStyle>();
                    for (int i = 0; i < datacol; i++)
                    {
                        rowStyle.Add(sheet.GetRow(rowStart).GetCell(i).CellStyle);
                    }

                    //Thêm row
                    int k = 0;
                    foreach (var item in data)
                    {
                        try
                        {
                            XSSFRow row = (XSSFRow)sheet.CreateRow(rowStart);
                            for (int i = 0; i < datacol; i++)
                            {
                                row.CreateCell(i).CellStyle = rowStyle[i];

                                if (i == 0)
                                {
                                    row.GetCell(i).SetCellValue(k + 1);
                                }
                                else if (i == 1)
                                {
                                    row.GetCell(i).SetCellValue(item.Note);
                                }
                                else if (i == 2)
                                {
                                    if(item.StartValue != null)
                                        row.GetCell(i).SetCellValue((double)item.StartValue);
                                }
                                else if (i == 3)
                                {
                                    if (item.EndValue != null)
                                        row.GetCell(i).SetCellValue((double)item.EndValue);
                                }
                                else if (i == 4)
                                {
                                    if (item.MainPriceGreater5M != null)
                                        row.GetCell(i).SetCellValue((double)item.MainPriceGreater5M);
                                }
                                else if (i == 5)
                                {
                                    if (item.ExtraPriceGreater5M != null)
                                        row.GetCell(i).SetCellValue((double)item.ExtraPriceGreater5M);
                                }
                                else if (i == 6)
                                {
                                    if (item.MainPriceLess5M != null)
                                        row.GetCell(i).SetCellValue((double)item.MainPriceLess5M);
                                }
                                else if (i == 7)
                                {
                                    if (item.ExtraPriceLess5M != null)
                                        row.GetCell(i).SetCellValue((double)item.ExtraPriceLess5M);
                                }
                                else if (i == 8)
                                {
                                    if (item.MainPriceLess3M != null)
                                        row.GetCell(i).SetCellValue((double)item.MainPriceLess3M);
                                }
                                else if (i == 9)
                                {
                                    if (item.ExtraPriceLess3M != null)
                                        row.GetCell(i).SetCellValue((double)item.ExtraPriceLess3M);
                                }
                                else if (i == 10)
                                {
                                    if (item.MainPriceLess2M != null)
                                        row.GetCell(i).SetCellValue((double)item.MainPriceLess2M);
                                }
                                else if (i == 11)
                                {
                                    if (item.ExtraPriceLess2M != null)
                                        row.GetCell(i).SetCellValue((double)item.ExtraPriceLess2M);
                                }
                            }
                            rowStart++;
                            k++;
                        }
                        catch (Exception ex)
                        {
                            log.Error("ExportDataExcel:" + ex);
                        }
                    }
                }
                catch (Exception ex)
                {
                    log.Error("ExportDataExcel:" + ex);
                }
            }

            sheet.ForceFormulaRecalculation = true;

            MemoryStream ms = new MemoryStream();

            workbook.Write(ms);

            return ms;
        }
