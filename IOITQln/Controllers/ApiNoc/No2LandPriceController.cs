        [HttpPost("getLandScapePrice61")]
        public async Task<IActionResult> getLandScapePrice61([FromBody] No2LandPriceInput input)
        {
            string accessToken = Request.Headers[HeaderNames.Authorization];
            Entities.Token token = (from t in _context.Tokens where Convert.ToString(t.AccessToken) == accessToken select t).FirstOrDefault();
            if (token == null)
            {
                return Unauthorized();
            }

            DefaultResponse def = new DefaultResponse();
            //check role
            var identity = (ClaimsIdentity)User.Identity;
            string access_key = identity.Claims.Where(c => c.Type == "AccessKey").Select(c => c.Value).SingleOrDefault();
            if (!CheckRole.CheckRoleByCode(access_key, functionCode, (int)AppEnums.Action.VIEW))
            {
                def.meta = new Meta(222, ApiConstants.MessageResource.NOPERMISION_VIEW_MESSAGE);
                return Ok(def);
            }

            try
            {
                double? landScapePrice_61 = null;
                No2LandPrice data = null;
                data = _context.No2LandPricies.Where(e => e.StartValue <= input.LandPriceItemValue_61 && e.EndValue > input.LandPriceItemValue_61 && e.Status != AppEnums.EntityStatus.DELETED).FirstOrDefault();

                if(data == null)
                {
                    data = _context.No2LandPricies.Where(e => e.StartValue <= input.LandPriceItemValue_61 && e.Status != AppEnums.EntityStatus.DELETED).OrderByDescending(e => e.StartValue).FirstOrDefault();
                }

                if (data != null)
                {
                    switch(true)
                    {
                        case true when input.Width > 5:
                            landScapePrice_61 = input.TypeAlley == AppEnums.TypeAlley.MAIN ? data.MainPriceGreater5M : data.ExtraPriceGreater5M;
                            break;
                        case true when input.Width >= 3 && input.Width <= 5:
                            landScapePrice_61 = input.TypeAlley == AppEnums.TypeAlley.MAIN ? data.MainPriceLess5M : data.ExtraPriceLess5M;
                            break;
                        case true when input.Width >= 2 && input.Width < 3:
                            landScapePrice_61 = input.TypeAlley == AppEnums.TypeAlley.MAIN ? data.MainPriceLess3M : data.ExtraPriceLess3M;
                            break;
                        case true when input.Width < 2:
                            landScapePrice_61 = input.TypeAlley == AppEnums.TypeAlley.MAIN ? data.MainPriceLess2M : data.ExtraPriceLess2M;
                            break;
                        default:
                            break;
                    }

                    if(landScapePrice_61 == null)
                    {
                        def.meta = new Meta(404, "Không tìm thấy giá trị ở Bảng giá đất số 2 cho số tiền: " + input.LandPriceItemValue_61.ToString("N0") + ", chiều rộng hẻm " + input.Width + ", Loại hẻm: " + (input.TypeAlley == AppEnums.TypeAlley.MAIN ? "Đất nằm mặt tiền hẻm chính" : "Đất nằm ở hẻm phụ"));
                        return Ok(def);
                    }
                }
                else
                {
                    def.meta = new Meta(404, "Không tìm thấy cấu hình Bảng giá đất số 2 cho số tiền: " + input.LandPriceItemValue_61.ToString("N0"));
                    return Ok(def);
                }

                def.meta = new Meta(200, ApiConstants.MessageResource.ACCTION_SUCCESS);
                def.data = landScapePrice_61;
                return Ok(def);
            }
            catch (Exception ex)
            {
                log.Error("GetById Error:" + ex);
                def.meta = new Meta(500, ApiConstants.MessageResource.ERROR_500_MESSAGE);
                return Ok(def);
            }
        }

